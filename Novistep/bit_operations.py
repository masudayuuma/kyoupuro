# =============================================
# ビット演算まとめ (Bit Operations)
# =============================================

# ■ 基本の演算子一覧
# -----------------------------------------------
# 演算子  名前         例          結果
# &      AND         5 & 3    →  1
# |      OR          5 | 3    →  7
# ^      XOR         5 ^ 3    →  6
# ~      NOT(反転)    ~5       → -6
# <<     左シフト     1 << 3   →  8
# >>     右シフト     8 >> 3   →  1
# -----------------------------------------------

# =============================================
# 1. AND (&) : 両方が1のビットだけ1
# =============================================
# 5  = 101
# 3  = 011
# --------
# &  = 001 = 1
print("=== AND ===")
print(5 & 3)   # 1
print(0b101 & 0b011)  # 2進数で書いても同じ

# 用途: 特定のビットが立っているか確認
# i番目のビットが1かどうか
n = 0b1010  # 10
i = 1
print((n >> i) & 1)  # 1 (i番目が1)

# =============================================
# 2. OR (|) : どちらかが1のビットは1
# =============================================
# 5  = 101
# 3  = 011
# --------
# |  = 111 = 7
print("\n=== OR ===")
print(5 | 3)   # 7

# 用途: 特定のビットを立てる
n = 0b1000  # 8
n = n | (1 << 2)  # 2番目のビットを立てる
print(bin(n))  # 0b1100

# =============================================
# 3. XOR (^) : 違うビットだけ1 (排他的論理和)
# =============================================
# 5  = 101
# 3  = 011
# --------
# ^  = 110 = 6
print("\n=== XOR ===")
print(5 ^ 3)   # 6

# XOR の重要な性質
# ① a ^ a = 0  (自分自身とXORすると0)
print(7 ^ 7)   # 0
# ② a ^ 0 = a  (0とXORすると元の値)
print(7 ^ 0)   # 7
# ③ a ^ b ^ b = a  (2回XORすると元に戻る)
a, b = 5, 3
print(a ^ b ^ b)  # 5

# 用途①: 奇数回だけ出てくる要素を見つける
# → 同じ値を2回XORすると打ち消し合う
nums = [1, 2, 3, 2, 1]
result = 0
for x in nums:
    result ^= x
print(result)  # 3 (奇数回出てくる要素)

# 用途②: 暗号化・スワップ
a, b = 5, 3
a ^= b
b ^= a
a ^= b
print(a, b)  # 3, 5 (swap完了)

# =============================================
# 4. NOT (~) : ビットを全て反転
# =============================================
# Pythonでは ~n = -(n+1)
print("\n=== NOT ===")
print(~5)   # -6
print(~0)   # -1

# =============================================
# 5. 左シフト (<<) : ビットを左にずらす = ×2
# =============================================
# 1 << 3 → 1を3つ左へ → 1000(2) = 8
print("\n=== 左シフト <<  ===")
print(1 << 0)  # 1
print(1 << 1)  # 2
print(1 << 2)  # 4
print(1 << 3)  # 8
print(1 << 4)  # 16
# n << k は n * (2^k) と同じ

# 競プロ頻出: n要素の全部分集合を列挙
# 1 << n で 2^n 個の状態を表す
n = 3
for mask in range(1 << n):
    subset = []
    for i in range(n):
        if (mask >> i) & 1:
            subset.append(i)
    print(f"mask={mask:03b}: {subset}")

# =============================================
# 6. 右シフト (>>) : ビットを右にずらす = //2
# =============================================
# 8 >> 1 → 8 // 2 = 4
print("\n=== 右シフト >> ===")
print(8 >> 1)  # 4
print(8 >> 2)  # 2
print(8 >> 3)  # 1

# i番目のビットを取り出す
n = 0b10110  # 22
for i in range(5):
    print(f"bit[{i}] = {(n >> i) & 1}")

# =============================================
# 7. よく使うビット演算テクニック
# =============================================
print("\n=== テクニック集 ===")

# ① 2の累乗かどうか判定
# 2^k は 100...0 → 1引くと 011...1 → AND すると 0
def is_power_of_2(n):
    return n > 0 and (n & (n - 1)) == 0

print(is_power_of_2(8))   # True
print(is_power_of_2(6))   # False

# ② 1のビット数を数える (popcount)
n = 0b10110  # 22
print(bin(n).count('1'))  # 2 (Pythonらしいやり方)
print(n.bit_count())       # 2 (Python 3.10以降)

# ③ 最下位の1のビットだけ取り出す
n = 0b10110
lsb = n & (-n)
print(bin(lsb))  # 0b10

# ④ 最下位の1のビットを消す
n = 0b10110
n = n & (n - 1)
print(bin(n))  # 0b10100

# ⑤ i番目のビットを立てる (set)
n = 0b1000
n |= (1 << 2)
print(bin(n))  # 0b1100

# ⑥ i番目のビットを消す (clear)
n = 0b1110
n &= ~(1 << 1)
print(bin(n))  # 0b1100

# ⑦ i番目のビットを反転 (toggle)
n = 0b1010
n ^= (1 << 1)
print(bin(n))  # 0b1000

# =============================================
# 8. 競プロ頻出パターン
# =============================================

# パターン①: 部分集合の全列挙 (ビット全探索)
# n個の要素に対して 2^n 通り全部試す
print("\n=== ビット全探索 ===")
items = ['A', 'B', 'C']
n = len(items)
for mask in range(1 << n):
    selected = [items[i] for i in range(n) if (mask >> i) & 1]
    print(selected)

# パターン②: XORで「出現奇数回の要素」を求める
print("\n=== XOR で奇数回の要素を探す ===")
arr = [4, 1, 2, 1, 2]
ans = 0
for x in arr:
    ans ^= x
print(ans)  # 4

# パターン③: bit_count() で集合のサイズを測る
mask = 0b10110
print(f"選ばれた要素数: {mask.bit_count()}")  # 3

# =============================================
# 9. 2進数表示の便利な書き方
# =============================================
print("\n=== 表示 ===")
n = 22
print(bin(n))           # 0b10110
print(format(n, 'b'))   # 10110
print(format(n, '08b')) # 00010110 (8桁ゼロ埋め)
print(f"{n:08b}")       # 00010110 (f-stringでも同様)

# 2進数 → 10進数
print(int('10110', 2))  # 22
print(0b10110)          # 22
